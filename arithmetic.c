/* This Program Demonstrates C Arithmetic */

#include <stdio.h>

void main(void)
{ int i=5, j=6, k;
   
  printf("i+j=%d\n", k=i+j);  
  printf("i-j=%d\n", k=i-j);
  printf("i*j=%d\n", k=i*j);
  printf("i/j=%d\n", k=i/j); 
  printf("j/i=%d\n", k=j/i);
  printf("i%%j=%d\n", k=i%j);
  printf("j%%i=%d\n", k=j%i);
  printf("++j=%d\n", k=++j);
  printf("--i=%d\n", k=--i); 
}
